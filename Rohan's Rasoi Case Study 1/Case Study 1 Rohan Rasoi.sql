CREATE DATABASE rohan_db;
USE rohan_db;
CREATE TABLE menu (
    product_id INT PRIMARY KEY,
    product_name VARCHAR(255),
    price DECIMAL(10, 2)
);

CREATE TABLE member (
    customer_id INT PRIMARY KEY,
    join_date DATE
);
CREATE TABLE Sales (
    sale_id INT PRIMARY KEY,
    customer_id INT,
    product_id INT,
    sale_date DATE,
    FOREIGN KEY (product_id) REFERENCES menu(product_id)
);
